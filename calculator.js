// Define the numbers and the operation here
const x = 8;
const y = 2;
const operator = "+";
// Write if-statements here
if (operator === "+") {
  console.log(x + y);
} else if (operator === "/") {
  console.log(x / y);
} else if (operator === "-") {
  console.log(x - y);
} else if (operator === "*") {
  console.log(x * y);
}
